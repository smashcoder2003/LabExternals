import unittest
import DataStructures.DrivenCode.QuickSort.main as Qs


class QuickSortTest(unittest.TestCase):
    def setUp(self):
        self.test1 = Qs.startQuickSort([62, 18, 100, 92, 81, 35, 74, 82, 83, 47, 70, 25, 72, 69, 67, 64])
        self.test2 = Qs.startQuickSort([27, 24, 76, 70, 72, 59, 75, 77, 96, 99, 51, 3, 45, 60, 46, 58, 37, 8, 86, 100])
        self.test3 = Qs.startQuickSort(
            [67, 16, 21, 70, 88, 92, 46, 64, 73, 75, 65, 90, 9, 80, 36, 6, 97, 39, 4, 72, 43, 69, 49, 47, 23, 95, 41,
             14, 31, 13])

    def test_1(self):
        statement = "\n----------------------------------------------------------------------\narray : {}" \
                    "\nExpected : {} \n Actual : {}".format(
            [62, 18, 100, 92, 81, 35, 74, 82, 83, 47, 70, 25, 72, 69, 67, 64],
            [18, 25, 35, 47, 62, 64, 67, 69, 70, 72, 74, 81, 82, 83, 92, 100],
            self.test1)
        self.assertEquals(self.test1, [18, 25, 35, 47, 62, 64, 67, 69, 70, 72, 74, 81, 82, 83, 92, 100], statement)

    def test_2(self):
        statement = "\n----------------------------------------------------------------------\narray : {}" \
                    "\nExpected : {} \n Actual : {}".format(
            [27, 24, 76, 70, 72, 59, 75, 77, 96, 99, 51, 3, 45, 60, 46, 58, 37, 8, 86, 100],
            [3, 8, 24, 27, 37, 45, 46, 51, 58, 59, 60, 70, 72, 75, 76, 77, 86, 96, 99, 100],
            self.test2)
        self.assertEquals(self.test2, [3, 8, 24, 27, 37, 45, 46, 51, 58, 59, 60, 70, 72, 75, 76, 77, 86, 96, 99, 100],
                          statement)

    def test_3(self):
        statement = "\n----------------------------------------------------------------------\narray : {}" \
                    "\nExpected : {} \n Actual : {}".format(
            [67, 16, 21, 70, 88, 92, 46, 64, 73, 75, 65, 90, 9, 80, 36, 6, 97, 39, 4, 72, 43, 69, 49, 47, 23, 95, 41,
             14, 31, 13],
            [4, 6, 9, 13, 14, 16, 21, 23, 31, 36, 39, 41, 43, 46, 47, 49, 64, 65, 67, 69, 70, 72, 73, 75, 80, 88, 90,
             92, 95, 97],
            self.test3)

        self.assertEquals(self.test3,
                          [4, 6, 9, 13, 14, 16, 21, 23, 31, 36, 39, 41, 43, 46, 47, 49, 64, 65, 67, 69, 70, 72, 73, 75,
                           80, 88, 90, 92, 95, 97])
